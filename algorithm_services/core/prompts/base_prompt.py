from typing import Dict, Optional
from abc import ABC, abstractmethod


class BasePrompt(ABC):
    """Prompt基类"""
    
    @classmethod
    @abstractmethod
    def get_system_prompt(cls) -> str:
        """获取系统Prompt"""
        pass
    
    @classmethod
    @abstractmethod
    def build_user_prompt(cls, *args, **kwargs) -> str:
        """构建用户Prompt"""
        pass


