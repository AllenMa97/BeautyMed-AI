def get_base_system_prompt(custom_rules: Optional[str] = None, minor_mode: bool = False) -> str:
    """
    获取全局通用System Prompt，支持追加自定义规则
    :param custom_rules: 功能专属的自定义规则（可选）
    :param minor_mode: 是否启用未成年人模式（默认False）
    :return: 拼接后的完整System Prompt
    """
    if minor_mode:
        base_prompt = GLOBAL_SYSTEM_PROMPT_MINOR.strip()
    else:
        base_prompt = GLOBAL_SYSTEM_PROMPT.strip()
    if custom_rules:
        base_prompt += f"\n\n【功能专属规则】\n{custom_rules.strip()}"
    return base_prompt

# 通用Prompt填充工具（所有功能复用）
def fill_prompt_template(template: str, **kwargs) -> str:
    """
    填充Prompt模板中的变量
    :param template: Prompt模板（含{变量名}）
    :param kwargs: 变量键值对
    :return: 填充后的Prompt
    """
    try:
        return template.format(**kwargs)
    except KeyError as e:
        raise ValueError(f"Prompt模板缺失变量：{e}")
